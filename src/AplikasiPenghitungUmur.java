        private JSONArray fetchJsonArrayFromUrl(String urlStr) throws Exception {
        URL url = new URL(urlStr);
        HttpURLConnection con = (HttpURLConnection) url.openConnection();
        con.setRequestMethod("GET");
        con.setConnectTimeout(5000);
        con.setReadTimeout(7000);
        con.setRequestProperty("Accept", "application/json");

        int status = con.getResponseCode();
        BufferedReader in;
        if (status >= 200 && status < 300) {
            in = new BufferedReader(new InputStreamReader(con.getInputStream(), "UTF-8"));
        } else {
            in = new BufferedReader(new InputStreamReader(con.getErrorStream(), "UTF-8"));
        }

        StringBuilder content = new StringBuilder();
        String line;
        while ((line = in.readLine()) != null) {
            content.append(line);
        }
        in.close();
        con.disconnect();

        // parsing: beberapa API mengembalikan object { data: [...] } atau langsung array [...]
        String raw = content.toString().trim();
        if (raw.isEmpty()) return null;

        if (raw.startsWith("[")) {
            return new JSONArray(raw);
        } else if (raw.startsWith("{")) {
            JSONObject obj = new JSONObject(raw);
            // coba beberapa kemungkinan key yang berisi array
            if (obj.has("data") && obj.get("data") instanceof JSONArray) {
                return obj.getJSONArray("data");
            } else if (obj.has("holidays") && obj.get("holidays") instanceof JSONArray) {
                return obj.getJSONArray("holidays");
            } else if (obj.has("result") && obj.get("result") instanceof JSONArray) {
                return obj.getJSONArray("result");
            } else {
                // Jika isi object tapi tidak ketemu array, kembalikan array kosong
                return new JSONArray();
            }
        } else {
            return new JSONArray();
        }
    }
    
