    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        Date tanggalLahir = dateChooserTanggalLahir.getDate();
        if (tanggalLahir == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih tanggal lahir terlebih dahulu!");
            return;
        }

        LocalDate lahir = tanggalLahir.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate sekarang = LocalDate.now();
        Period selisih = Period.between(lahir, sekarang);

        txtUmur.setText(String.format("%d tahun, %d bulan, %d hari",
            selisih.getYears(), selisih.getMonths(), selisih.getDays()));

        // Hitung ulang tahun berikutnya
        LocalDate ulangTahunBerikutnya = lahir.withYear(sekarang.getYear());
        if (!ulangTahunBerikutnya.isAfter(sekarang)) {
            ulangTahunBerikutnya = ulangTahunBerikutnya.plusYears(1);
        }

        Period menujuUlangTahun = Period.between(sekarang, ulangTahunBerikutnya);
        txtUlangTahunBerikutnya.setText(String.format("%s (dalam %d bulan %d hari lagi)",
            ulangTahunBerikutnya.toString(), menujuUlangTahun.getMonths(), menujuUlangTahun.getDays()));

        // --- Panggil API hari libur untuk bulan & tahun ulangTahunBerikutnya ---
        try {
            int queryMonth = ulangTahunBerikutnya.getMonthValue();
            int queryYear = ulangTahunBerikutnya.getYear();

            // contoh API publik: https://api-harilibur.vercel.app/api?month=8&year=2020
            String apiUrl = String.format("https://calendarific.com/api/v2/holidays?api_key=YOUR_API_KEY&country=ID&year=%d&month=%d", queryMonth, queryYear);

            JSONArray holidays = fetchJsonArrayFromUrl(apiUrl);

            // Periksa apakah salah satu holiday berisi tanggal yang sama`
            String found = null;
            String targetDate = ulangTahunBerikutnya.toString(); // "YYYY-MM-DD"
            if (holidays != null) {
                for (int i = 0; i < holidays.length(); i++) {
                    JSONObject h = holidays.getJSONObject(i);
                    // Struktur JSON bisa berbeda tergantung API: cari properti "date" atau "holiday_date" / "tanggal"
                    String dateStr = null;
                    if (h.has("date")) dateStr = h.optString("date", null);
                    else if (h.has("tanggal")) dateStr = h.optString("tanggal", null);
                    else if (h.has("holiday_date")) dateStr = h.optString("holiday_date", null);
                    else if (h.has("holiday")) {
                        // holiday bisa berupa objek
                        Object holidayObj = h.get("holiday");
                        if (holidayObj instanceof JSONObject) {
                            JSONObject ho = (JSONObject) holidayObj;
                            if (ho.has("date")) dateStr = ho.optString("date", null);
                        }
                    }

                    // nama acara
                    String name = null;
                    if (h.has("name")) name = h.optString("name", null);
                    else if (h.has("libur")) name = h.optString("libur", null);
                    else if (h.has("keterangan")) name = h.optString("keterangan", null);
                    else if (h.has("holiday")) {
                        Object holidayObj = h.get("holiday");
                        if (holidayObj instanceof JSONObject) {
                            JSONObject ho = (JSONObject) holidayObj;
                            if (ho.has("name")) name = ho.optString("name", null);
                        }
                    }

                    if (dateStr != null && dateStr.startsWith(targetDate)) {
                        found = (name != null ? name : "Libur / acara khusus");
                        break;
                    }
                    // beberapa API menyimpan tanggal dalam format "dd-mm-yyyy", jadi cek juga kemungkinan itu
                    if (dateStr != null && dateStr.contains("-")) {
                        // try dd-mm-yyyy
                        String[] parts = dateStr.split("-");
                        if (parts.length == 3) {
                            String alt = String.format("%s-%s-%s", parts[2], parts[1], parts[0]);
                            if (alt.equals(targetDate)) {
                                found = (name != null ? name : "Libur / acara khusus");
                                break;
                            }
                        }
                    }
                }
            }

            if (found != null) {
                txtAcaraSpesial.setText(found + " (Libur Nasional)");
            } else {
                // kalau tidak ditemukan dari API, fallback ke check manual sederhana (MonthDay)
                MonthDay md = MonthDay.from(ulangTahunBerikutnya);
                // contoh manual: cek 1 Jan, 17 Aug, 25 Dec
                if (md.equals(MonthDay.of(1,1))) txtAcaraSpesial.setText("Tahun Baru Masehi");
                else if (md.equals(MonthDay.of(8,17))) txtAcaraSpesial.setText("Hari Kemerdekaan RI");
                else if (md.equals(MonthDay.of(12,25))) txtAcaraSpesial.setText("Hari Natal");
                else txtAcaraSpesial.setText("Tidak ada acara spesial terdaftar pada hari itu.");
            }

        } catch (Exception ex) {
            // Kalau gagal memanggil API (no internet / error), beri pesan informatif dan jangan crash
            txtAcaraSpesial.setText("Gagal ambil data hari libur (cek koneksi).");
            System.err.println("Error saat memanggil API hari libur: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnHitungActionPerformed

